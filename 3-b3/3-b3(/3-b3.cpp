/*仅限十亿位都满的猥琐办法..*/
#include <iostream>

using namespace std;

int main()
{
   char a,b,c,d,e,f,g,h,i,j,k,l,m;
   cin>>a>>b>>c>>d>>e>>f>>g>>h>>i>>j>>k>>l>>m;
   cout<<"十亿位"<<a<<endl;
   cout<<"亿位"<<b<<endl;
   cout<<"千万位"<<c<<endl;
   cout<<"百万位"<<d<<endl;
   cout<<"十万位"<<e<<endl;
   cout<<"万位"<<f<<endl;
   cout<<"千位"<<g<<endl;
   cout<<"百位"<<h<<endl;
   cout<<"十位"<<i<<endl;
   cout<<"个位"<<j<<endl;
   cout<<"十分位"<<l<<endl;
   cout<<"百分位"<<m<<endl;
    return 0;
}
