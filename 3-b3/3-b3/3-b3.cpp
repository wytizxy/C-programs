#include <iostream>

using namespace std;

int main()
{
    double a;
    unsigned b,c,d,e,f,g,h,i,j,k,l,m;
    cin>>a;
    b=a/1000000000;
    c=(a-1000000000*b)/100000000;
    d=(a-1000000000*b-100000000*c)/10000000;
    e=(a-1000000000*b-100000000*c-10000000*d)/1000000;
    f=(a-1000000000*b-100000000*c-10000000*d-1000000*e)/100000;
    g=(a-1000000000*b-100000000*c-10000000*d-1000000*e-100000*f)/10000;
    h=(a-1000000000*b-100000000*c-10000000*d-1000000*e-100000*f-10000*g)/1000;
    i=(a-1000000000*b-100000000*c-10000000*d-1000000*e-100000*f-10000*g-1000*h)/100;
    j=(a-1000000000*b-100000000*c-10000000*d-1000000*e-100000*f-10000*g-1000*h-100*i)/10;
    k=(a-1000000000*b-100000000*c-10000000*d-1000000*e-100000*f-10000*g-1000*h-100*i-10*j)/1;
    l=(a-1000000000*b-100000000*c-10000000*d-1000000*e-100000*f-10000*g-1000*h-100*i-10*j-k)/0.1;
    m=(a-1000000000*b-100000000*c-10000000*d-1000000*e-100000*f-10000*g-1000*h-100*i-10*j-k-0.1*l)/0.01;

    cout <<"十亿位"<< b<<endl;
    cout<< "亿位"<<c<<endl;
    cout<<"千万位"<<d<<endl;
    cout<<"百万位"<<e<<endl;
    cout<<"十万位"<<f<<endl;
    cout<<"万位"<<g<<endl;
    cout<<"千位"<<h<<endl;
    cout<<"百位"<<i<<endl;
    cout<<"十位"<<j<<endl;
    cout<<"个位"<<k<<endl;
    cout<<"十分位"<<l<<endl;
    cout<<"百分位"<<m<< endl;
    return 0;
}
